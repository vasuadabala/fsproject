// print 1 to 10 numbers
// i++ incrementor -> after the loop i gets incremented
// ++i incrementor
// i-- decrementor
// --i decrementor
// for loop syntax middle snytax is condition
function printtennumbers() {
    for (var i = 1; i <= 10; i++) { // start of loop 
        console.log(i);
    } // end of loop
}
printtennumbers();
