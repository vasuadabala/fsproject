//2.area of a rectangle
//this method is input as length and width and give us output
//area of the triangle is the product of length and width
function calculateAreaOfRectangle(length, width) {
  let areaOfRectangle = 0 ;
  areaOfRectangle = length * width ;
  console.log("area of rectangle is ", areaOfRectangle);
  return areaOfRectangle;
}

calculateAreaOfRectangle(6, 8);
calculateAreaOfRectangle(9, 6);
